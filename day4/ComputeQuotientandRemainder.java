public class ComputeQuotientandRemainder {
    public static void main(String[] args) {
        int dividend = 17;
        int divisor = 5;
        // Computing quotient
        int quotient = dividend / divisor;
        // Computing remainder
        int remainder = dividend % divisor;
        System.out.println("Quotient: " + quotient+" Remainder: " + remainder);
    }
}